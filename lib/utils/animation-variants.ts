/**
 * Fade in only (for reduced motion)
 */
export const fadeVariants: Variants = {
  hidden: {
    opacity: 0,
  },
  visible: {
    opacity: 1,
    transition: {
      duration: 0.3,
    },
  },
};

/**
 * Get animation variants based on reduced motion preference
 */
export function getAnimationVariants(baseVariants: Variants): Variants {
  if (prefersReducedMotion()) {
    return fadeVariants;
  }
  return baseVariants;
}
